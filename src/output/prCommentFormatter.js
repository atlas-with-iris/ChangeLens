// ═══════════════════════════════════════════════════════════════════════
// PR COMMENT FORMATTER — Render Impact Card as GitHub-flavored markdown
//
// Design goals:
//   - Visually clean
//   - Instant comprehension
//   - One memorable card
//   - Risk tier visible in first 3 seconds
//
// Language discipline (Iris directive):
//   Use "estimated", "detected", "identified" — not certainty language.
//   The engine earns trust through evidence, not assertion.
// ═══════════════════════════════════════════════════════════════════════

const RISK_BADGES = {
  LOW: "🟢 LOW",
  MEDIUM: "🟡 MEDIUM",
  HIGH: "🔴 HIGH",
};

const RISK_HEADERS = {
  LOW: "Low structural risk detected",
  MEDIUM: "Review recommended — potential impact detected",
  HIGH: "Potential breaking change detected",
};

/**
 * Render an Impact Card as a GitHub PR comment in markdown.
 *
 * @param {import('./impactCardBuilder.js').ImpactCard} card
 * @returns {string} - GitHub-flavored markdown
 */
export function formatPrComment(card) {
  const badge = RISK_BADGES[card.riskTier] || "⚪ UNKNOWN";
  const header = RISK_HEADERS[card.riskTier] || "Unknown risk";

  const lines = [];

  // ── Header ──────────────────────────────────────────────────────
  lines.push(`## 🔍 ChangeLens — Impact Estimate`);
  lines.push(``);
  lines.push(`> **${badge}** — ${header}`);
  lines.push(``);

  // ── Summary ─────────────────────────────────────────────────────
  lines.push(`### Summary`);
  lines.push(card.summary);
  lines.push(``);

  // ── Why ──────────────────────────────────────────────────────────
  lines.push(`### Why this risk tier?`);
  lines.push(card.why);
  lines.push(``);

  // ── Changed Surfaces ────────────────────────────────────────────
  if (card.changedSurfaces.length > 0) {
    lines.push(`### Affected Surfaces Detected`);
    for (const surface of card.changedSurfaces) {
      lines.push(`- \`${surface}\``);
    }
    lines.push(``);
  }

  // ── Affected Consumers ──────────────────────────────────────────
  if (card.affectedConsumers.length > 0) {
    lines.push(`### Downstream Consumers Identified`);
    lines.push(`${card.affectedConsumers.length} file(s) import from changed surfaces:`);
    lines.push(``);
    for (const consumer of card.affectedConsumers.slice(0, 10)) {
      lines.push(`- \`${consumer}\``);
    }
    if (card.affectedConsumers.length > 10) {
      lines.push(`- _...and ${card.affectedConsumers.length - 10} more_`);
    }
    lines.push(``);
  }

  // ── Evidence ────────────────────────────────────────────────────
  if (card.evidence.length > 0) {
    lines.push(`### Evidence`);
    for (const item of card.evidence) {
      lines.push(`- ${item}`);
    }
    lines.push(``);
  }

  // ── Merge Guidance ──────────────────────────────────────────────
  lines.push(`---`);
  lines.push(``);

  if (card.safeToMerge) {
    lines.push(`✅ **Safe to merge** — ${card.mergeCaution}`);
  } else {
    lines.push(`⚠️ **${card.mergeCaution}**`);
  }

  lines.push(``);
  lines.push(`<sub>Generated by ChangeLens · Static analysis impact estimate · Scope: import graph + surface heuristics · Not a guarantee</sub>`);

  return lines.join("\n");
}

/**
 * Render a compact one-line summary for CI status checks.
 *
 * @param {import('./impactCardBuilder.js').ImpactCard} card
 * @returns {string}
 */
export function formatStatusLine(card) {
  const badge = RISK_BADGES[card.riskTier] || "⚪";
  return `${badge} | ${card.summary}`;
}
